// -----------------------------------------------------------------------------------------

function notpointlessquestion(msg, answer, FailMsg, SuccessMsg) {
    let response = prompt(msg, "");
    if (answer.toLowerCase() !== response.toLowerCase()) {
        alert(FailMsg)
        notpointlessquestion(msg, answer, FailMsg, SuccessMsg)
    } else {
        alert(SuccessMsg)
    }
}

// -----------------------------------------------------------------------------------------

function noresponsequestion(msg, answer, FailMsg) {
    let response = prompt(msg, "");
    if (answer.toLowerCase() !== response.toLowerCase()) {
        alert(FailMsg)
        noresponsequestion(msg, answer, FailMsg)
    }
}